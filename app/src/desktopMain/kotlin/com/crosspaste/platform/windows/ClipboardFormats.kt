package com.crosspaste.platform.windows

/**
 * Windows clipboard format constants
 */
object ClipboardFormats {
    const val CF_TEXT = 1 // ANSI text
    const val CF_BITMAP = 2 // Bitmap
    const val CF_METAFILEPICT = 3 // Metafile picture
    const val CF_SYLK = 4 // Microsoft Symbolic Link
    const val CF_DIF = 5 // Data Interchange Format
    const val CF_TIFF = 6 // Tagged Image File Format
    const val CF_OEMTEXT = 7 // OEM text
    const val CF_DIB = 8 // Device-independent bitmap
    const val CF_PALETTE = 9 // Color palette
    const val CF_PENDATA = 10 // Pen data
    const val CF_RIFF = 11 // RIFF audio
    const val CF_WAVE = 12 // Wave audio
    const val CF_UNICODETEXT = 13 // Unicode text
    const val CF_ENHMETAFILE = 14 // Enhanced metafile
    const val CF_HDROP = 15 // File list
    const val CF_LOCALE = 16 // Locale setting
}
